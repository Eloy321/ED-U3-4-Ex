/**
 * La clase OperacionResta tiene la funcion de restar.
 *
 * @author Eloy Beltrán López
 */
public class OperacionResta {

    /**
     * Metodo resta: resta dos números enteros 'x' e 'y'
     *
     * @param x Primer numero entero de la resta.
     * @param y Segundo nuemro entero de la resta.
     * @return Resultado de la resta (x-y).
     */
    public static int resta(int x, int y) {
        return x - y;
    }

    /**
     * Metodo resta: resta dos números decimales 'x' e 'y'
     *
     * @param x Primer numero decimal de la resta.
     * @param y Segundo nuemro decimal de la resta.
     * @return Resultado de la resta (x-y).
     */
    public static double resta(double x, double y) {
        return x - y;
    }

}
